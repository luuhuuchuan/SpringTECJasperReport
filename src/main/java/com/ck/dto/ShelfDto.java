package com.ck.dto;

public class ShelfDto {
	private String jan_code;
	private String goods_name;
	private int countShelf;

	public String getJan_code() {
		return jan_code;
	}

	public void setJan_code(String jan_code) {
		this.jan_code = jan_code;
	}

	public String getGoods_name() {
		return goods_name;
	}

	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}

	public int getCountShelf() {
		return countShelf;
	}

	public void setCountShelf(int countShelf) {
		this.countShelf = countShelf;
	}

}
